document.addEventListener("DOMContentLoaded", function () {
    function setDefaultLang() {
        localStorage.setItem("language", "en");
    }
    function getCurrentLang() {
        return localStorage.getItem("language") || "en";
    }

    function updateFormClassBasedOnLanguage() {
        const currentLang = getCurrentLang();
        if (currentLang === 'ta') {
            $('form').addClass('tamil-font');
        } else {
            $('form').removeClass('tamil-font');
        }
    }

    function updateLangContent() {
        const currentLang = getCurrentLang();
        console.log("Updating language content for:", currentLang);

        document.querySelectorAll("[data-i18n]").forEach((el) => {
            const key = el.getAttribute("data-i18n");
            if (languageContent[currentLang] && languageContent[currentLang][key]) {
                const textSpan = el.querySelector('.text');
                if (textSpan) {
                    textSpan.textContent = languageContent[currentLang][key];
                } else {
                    el.textContent = languageContent[currentLang][key];
                }
            }
        });
        updateLangToggleButton();
    }

    function updateLangToggleButton() {
        const toggleBtn = document.getElementById("langToggle");
        if (toggleBtn) {
            const labelSpan = toggleBtn.querySelector(".t-Button-label");
            const currentLang = getCurrentLang();
            if (labelSpan) {
                labelSpan.textContent = currentLang === "en" ? "தமிழ்" : "English";
            }
        }
    }


    function switchLang() {
        
        const currentLang = getCurrentLang();
        const newLang = currentLang === "en" ? "ta" : "en";
        localStorage.setItem("language", newLang);

        // After switching, update UI
        updateFormClassBasedOnLanguage();
        updateLangContent();


    }
    if (!localStorage.getItem("language")) {
        setDefaultLang();
    }
    updateFormClassBasedOnLanguage();
    updateLangContent();

    // Attach button click
    const toggleBtn = document.getElementById("langToggle");
    if (toggleBtn) {
        
        toggleBtn.addEventListener("click", function (e) {
            e.preventDefault();
            switchLang();
        });
    }

    // Listen for localStorage changes from other tabs
    window.addEventListener("storage", function () {
        updateFormClassBasedOnLanguage();
        updateLangContent();
    });
});
